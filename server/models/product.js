const mongoose = require("mongoose");
const productSchema = new mongoose.Schema({
  main_image: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  images: {
    type: Array,
    required: true,
  },
});
module.exports = mongoose.model("product", productSchema);
